drop table if exists comments cascade;
drop table if exists order_lines cascade;
drop table if exists orders cascade;
drop table if exists products cascade;
drop table if exists categories cascade;
drop table if exists users cascade;

create table categories (
    category_id bigint not null,
    description varchar(255) not null,
    highlighted boolean,
    icon varchar(255) not null,
    name varchar(255) not null,
    primary key (category_id)
);

create table users (
    user_id bigint generated by default as identity,
    address varchar(255) not null,
    card varchar(255),
    cvv integer,
    expiration_month integer,
    expiration_year integer,
    email varchar(255) not null,
    image varchar(255),
    name varchar(255) not null,
    password varchar(255) not null,
    reset_password_token varchar(255),
    primary key (user_id)
);

create table products (
    product_id bigint not null,
    description varchar(255) not null,
    icon varchar(255) not null,
    name varchar(255) not null,
    price integer not null,
    sales integer not null,
    total_comments integer not null,
    total_score integer not null,
    category bigint not null,
    primary key (product_id)
);

create table orders (
    order_id bigint generated by default as identity,
    address varchar(255) not null,
    name varchar(255) not null,
    price integer not null,
    state varchar(255) not null,
    timestamp bigint not null,
    user_id bigint not null,
    primary key (order_id)
);

create table order_lines (
    order_line_id bigint generated by default as identity,
    price integer,
    order_id bigint not null,
    product_id bigint not null,
    primary key (order_line_id)
);

create table comments (
    comment_id bigint generated by default as identity,
    rating integer not null,
    text varchar(255) not null,
    timestamp bigint not null,
    product_id bigint not null,
    user_id bigint not null,
    primary key (comment_id)
);

alter table categories
    add constraint uk_categories_name unique (name);

alter table products
    add constraint uk_products_name unique (name);

alter table users
    add constraint uk_users_email unique (email);

alter table products
    add constraint fk_products_category
    foreign key (category) references categories(category_id);

alter table orders
    add constraint fk_orders_user
    foreign key (user_id) references users(user_id);

alter table order_lines
    add constraint fk_order_lines_order
    foreign key (order_id) references orders(order_id);

alter table order_lines
    add constraint fk_order_lines_product
    foreign key (product_id) references products(product_id);

alter table comments
    add constraint fk_comments_product
    foreign key (product_id) references products(product_id);

alter table comments
    add constraint fk_comments_user
    foreign key (user_id) references users(user_id);
